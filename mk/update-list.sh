#!/bin/bash
set -euo pipefail
cd "$(dirname "${BASH_SOURCE[0]}")"/..

(
  cd csrc
  echo '# <auto-generated> mk/update-list.sh'
  echo 'csrc = files('
  find -name '*.c' -printf '%P\n' | sort -u | sed "s|.*|'\0'|" | paste -sd,
  echo ')'
) >csrc/meson.build

(
  echo '# <auto-generated> mk/update-list.sh'
  echo 'cgoflags_dirs = ['
  git grep -l '^import "C"$' '**/*.go' | xargs dirname | sort -u | sed "s|.*|'\0'|" | paste -sd,
  echo ']'
  echo 'cgostruct_dirs = ['
  git ls-files '**/cgostruct.in.go' | xargs dirname | sort -u | sed "s|.*|'\0'|" | paste -sd,
  echo ']'
  echo 'cgotest_dirs = ['
  git ls-files '**/*_ctest.go' | xargs dirname | sort -u | sed "s|.*|'\0'|" | paste -sd,
  echo ']'
) >mk/meson.build
